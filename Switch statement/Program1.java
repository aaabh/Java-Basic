public class Program1 {
  public static void main(String[] args) {
    int choice = 4;
    switch (choice) {
      case 1:
        System.out.println("COD");
        break;
      case 2:
        System.out.println("Card");
        break;
      case 3:
        System.out.println("UPI");
        break;
      case 4:
          System.out.println("Net Banking");
          break;
      default:
        System.out.println("Invalid choice");
    }
  }  
}
